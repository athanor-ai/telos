#!/usr/bin/env python3
"""lean_to_sv.py --- a follow-up pass generator: emit the SystemVerilog dual from
the Lean BBRv3 state-machine encoding.

The five pure sub-steps in `lean/BbrStarvation/Trace.lean`
(`filter_update`, `bandwidth_update`, `mode_transition`,
`pacing_gain_cycle`, `cwnd_compute`) and the ambient `BBRState W` record
map one-to-one onto SystemVerilog `always_ff` blocks. This generator
produces `verilog/bbr3.sv` so that the a follow-up pass k-induction filter
(the k-induction filter) can
cross-check the Lean theorems at bound K=64 RTTs.

Shape:
  - One `always_ff @(posedge clk)` block per sub-step, named to match
    the Lean identifier (e.g. `always_ff_filter_update`).
  - `BBRState W` fields become `reg`s; `Fin W -> Real` filter becomes a
    `reg [RTT_WIDTH-1:0] min_rtt_filt [0:W-1]` array.
  - Each SVA assertion mirrors one Lean theorem statement 1:1. The
    a follow-up pass EBMC harness runs these assertions as `--k-induction
    --bound 64`.

Status: **scaffold only**. Full generation lands under a follow-up pass once the
Lean sub-step bodies have been closed by the fleet (a follow-up pass). The stub
writes a placeholder `verilog/bbr3.sv` that declares the `bbr3` module
signature + empty `always_ff` skeletons so the EBMC preflight can
validate the module hierarchy without the real logic.

Usage:
    python3 experiments/lean_to_sv.py \\
        --lean-root lean/ \\
        --out verilog/bbr3.sv \\
        --w-rtt 100 \\
        --rtt-width 16
"""
from __future__ import annotations

import argparse
import pathlib
import sys


PLACEHOLDER_SV = """// Generated by experiments/lean_to_sv.py.
// SystemVerilog dual of lean/BbrStarvation/Trace.lean. Scaffold only;
// a follow-up pass fills the body after a follow-up pass closes the Lean sub-step bodies.

module bbr3 #(
    parameter int W = 100,
    parameter int RTT_WIDTH = 16
)(
    input  logic clk,
    input  logic reset,
    input  logic [RTT_WIDTH-1:0] ack_burst_size,
    input  logic [RTT_WIDTH-1:0] ack_delivered,
    input  logic [RTT_WIDTH-1:0] ack_wall_clock
);

    typedef enum logic [1:0] {STARTUP, DRAIN, PROBE_BW, PROBE_RTT} mode_t;

    reg mode_t mode;
    reg [RTT_WIDTH-1:0] pacing_rate;
    reg [RTT_WIDTH-1:0] inflight;
    reg [RTT_WIDTH-1:0] min_rtt_filt [0:W-1];
    reg [RTT_WIDTH-1:0] cwnd_gain;
    reg [RTT_WIDTH-1:0] pacing_gain;
    reg [2:0]           phase;
    reg [RTT_WIDTH-1:0] rt_elapsed;

    // Sub-step 1: filter_update (slide + insert). Body pending a follow-up pass.
    always_ff @(posedge clk) begin : always_ff_filter_update
        if (reset) begin
            // ... initial filter values ...
        end else begin
            // ... slide + insert ack sample ...
        end
    end

    // Sub-step 2: bandwidth_update. Body pending a follow-up pass.
    always_ff @(posedge clk) begin : always_ff_bandwidth_update
    end

    // Sub-step 3: mode_transition. Body pending a follow-up pass.
    always_ff @(posedge clk) begin : always_ff_mode_transition
    end

    // Sub-step 4: pacing_gain_cycle. Body pending a follow-up pass.
    always_ff @(posedge clk) begin : always_ff_pacing_gain_cycle
    end

    // Sub-step 5: cwnd_compute. Body pending a follow-up pass.
    always_ff @(posedge clk) begin : always_ff_cwnd_compute
    end

    // Assertions mirroring the 8 lemmas + main theorem in
    // lean/BbrStarvation/OnsetTheorem.lean. Names match the Lean
    // identifiers 1:1 so the EBMC verdict table can be merged with the
    // Lean close table on the same row-index.

    // minrtt_monotone
    // ack_agg_inflates
    // cwnd_gain_insufficient
    // onset_upper_bound
    // onset_lower_bound
    // arun_specialization
    // c_bounded_by_W
    // onset_tight
    // starves_within
endmodule
"""


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--lean-root", default="lean/", help="Lean project root")
    ap.add_argument("--out", default="verilog/bbr3.sv")
    ap.add_argument("--w-rtt", type=int, default=100)
    ap.add_argument("--rtt-width", type=int, default=16)
    args = ap.parse_args()

    lean_root = pathlib.Path(args.lean_root)
    if not lean_root.exists():
        print(f"ERROR: lean root not found: {lean_root}", file=sys.stderr)
        return 1

    # Scaffold: emit the placeholder. a follow-up pass replaces this with a real
    # parser over lean/BbrStarvation/Trace.lean that walks each sub-step
    # definition and emits the corresponding always_ff body.
    out_path = pathlib.Path(args.out)
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(PLACEHOLDER_SV)
    print(f"[lean_to_sv] wrote placeholder SV to {out_path}", file=sys.stderr)
    print("[lean_to_sv] a follow-up pass will fill the always_ff bodies + assertions after a follow-up pass closes.", file=sys.stderr)
    return 0


if __name__ == "__main__":
    sys.exit(main())
